// Condicionais e laços de repetição do TypeScript são semelhantes ao C
let condition = true;
if (condition) {
  // Ação
}

while (condition) {
  // Ação
}

for (let i = 0; condition; i++) {
  // Ação
}

// Operação ternária
condition = condition === true ? true : false;

// Operação acima em formato de if/else
if (condition) {
  condition = true;
} else {
  condition = false;
}
